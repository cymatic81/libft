void	*ft_memset(void *str, int x, size_t length)
{
	char	*b;
	char	nc;
	int		i;

	i = 0;
	&b = (char) *(*str);
	nc = (char) x;

	while (i < length)
	{
		&b[i] = nc;
		i++;
	}
	char *(*str) = *b;
}

